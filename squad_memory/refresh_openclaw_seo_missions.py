def task_component_state(task: Dict[str, Any], components_by_id: Dict[str, Dict[str, Any]]) -> Tuple[List[str], List[str]]:
    partial: List[str] = []
    missing: List[str] = []
    for component_id in task.get("stack", []):
        component = components_by_id.get(component_id, {})
        label = component.get("label", component_id)
        status = component.get("status", "missing")
        if status == "missing":
            missing.append(label)
        elif status == "partial":
            partial.append(label)
    return partial, missing


def build_execution_queue_payload(
    workflow: Dict[str, Any],
    evaluation: Dict[str, Any],
    components_by_id: Dict[str, Dict[str, Any]],
    route_source: str,
    goal: str | None = None,
) -> Dict[str, Any]:
    templates = workflow.get("queue_template", [])
    template_titles = {task["id"]: task["title"] for task in templates}
    items: List[Dict[str, Any]] = []

    for index, task in enumerate(templates, start=1):
        partial, missing = task_component_state(task, components_by_id)
        dependency_titles = [template_titles.get(dep, dep) for dep in task.get("depends_on", [])]

        blockers: List[str] = []
        if missing:
            blockers.extend(f"{label} is missing." for label in missing)
        elif dependency_titles:
            blockers.extend(f"Wait for {title}." for title in dependency_titles)
        elif partial:
            blockers.extend(f"{label} is only partially ready." for label in partial)

        if missing:
            status = "blocked"
        elif dependency_titles:
            status = "queued"
        elif partial:
            status = "at-risk"
        elif task.get("priority") == "now":
            status = "ready"
        else:
            status = "queued"

        task_id = f"{workflow['id']}.{task['id']}"
        artifact_root = f"automation/missions/execution/artifacts/{workflow['id']}"
        items.append(
            {
                "id": task_id,
                "order": index,
                "title": task["title"],
                "objective": task["objective"],
                "status": status,
                "priority": task["priority"],
                "owner": task["owner"],
                "stack": task.get("stack", []),
                "stack_labels": component_labels(task.get("stack", []), components_by_id),
                "deliverable": task["deliverable"],
                "depends_on": [f"{workflow['id']}.{dep}" for dep in task.get("depends_on", [])],
                "blocked_by": blockers,
                "artifact_markdown_path": f"{artifact_root}/{task['id']}.md",
                "artifact_json_path": f"{artifact_root}/{task['id']}.json",
            }
        )

    current_focus = next((item for item in items if item["status"] in {"ready", "at-risk"}), None)
    if not current_focus and items:
        current_focus = next((item for item in items if item["status"] == "blocked"), items[0])

    summary = {
        "total": len(items),
        "ready": sum(1 for item in items if item["status"] == "ready"),
        "at_risk": sum(1 for item in items if item["status"] == "at-risk"),
        "queued": sum(1 for item in items if item["status"] == "queued"),
        "blocked": sum(1 for item in items if item["status"] == "blocked"),
    }

    return {
        "generated_at": now_utc_iso(),
        "route_source": route_source,
        "goal": goal,
        "mission": {
            "id": workflow["id"],
            "name": workflow["name"],
            "score": evaluation["score"],
            "confidence": evaluation["confidence"],
            "objective": workflow["objective"],
            "output": workflow["output"],
            "outbox_path": f"automation/missions/outbox/{workflow['id']}.md",
        },
        "summary": summary,
        "why_now": evaluation["reasons"][:4],
        "current_focus": current_focus,
        "items": items,
        "context_files": [
            "automation/control/command-center.md",
            "automation/missions/router/mission-scoreboard.md",
            "../../memory/imports/codex/graph-hq/phase21/HQ_STATUS.md",
            "../../memory/imports/codex/graph-hq/phase31/GRAPH_STATUS.md",
            "automation/digest/latest.md",
            "automation/data/latest-status.md",
            f"automation/missions/prompts/{workflow['id']}.md",
        ],
    }


def build_progress_signals(
    workspace: Path,
    workflow: Dict[str, Any],
    queue_payload: Dict[str, Any],
) -> Dict[str, Any]:
    task_artifacts = {
        item["id"]: artifact_snapshot(workspace / item["artifact_markdown_path"])
        for item in queue_payload.get("items", [])
    }
    return {
        "hq_plan": artifact_snapshot(workspace / "automation" / "hq" / "current-plan.md"),
        "mission_outbox": artifact_snapshot(workspace / "automation" / "missions" / "outbox" / f"{workflow['id']}.md"),
        "task_artifacts": task_artifacts,
    }


def load_progress_state(path: Path) -> Dict[str, Any]:
    if not path.exists():
        return {"missions": {}}
    payload = load_json(path)
    if not isinstance(payload, dict):
        return {"missions": {}}
    if not isinstance(payload.get("missions"), dict):
        payload["missions"] = {}
    return payload


def save_progress_state(path: Path, payload: Dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, ensure_ascii=True) + "\n", encoding="utf-8")


def dependency_ready(item: Dict[str, Any], completed_ids: set[str]) -> bool:
    return all(dep in completed_ids for dep in item.get("depends_on", []))


def base_queue_status(item: Dict[str, Any]) -> str:
    if item["blocked_by"]:
        has_missing = any("missing" in blocker.lower() for blocker in item["blocked_by"])
        if has_missing:
            return "blocked"
        return "queued"
    return "at-risk" if item.get("status") == "at-risk" else "ready"


def apply_auto_progress(
    workspace: Path,
    workflow: Dict[str, Any],
    queue_payload: Dict[str, Any],
    progress_state_path: Path,
) -> Dict[str, Any]:
    state = load_progress_state(progress_state_path)
    mission_id = workflow["id"]
    mission_state = state["missions"].get(mission_id, {})
    completed_ids = {
        item_id
        for item_id in mission_state.get("completed_task_ids", [])
        if isinstance(item_id, str)
    }
    current_task_ids = {item["id"] for item in queue_payload["items"]}
    completed_ids &= current_task_ids

    signals = build_progress_signals(workspace, workflow, queue_payload)
    signal_history = mission_state.get("signals", {}) if isinstance(mission_state.get("signals"), dict) else {}
    artifact_history = mission_state.get("artifacts", {}) if isinstance(mission_state.get("artifacts"), dict) else {}
    advanced: List[Dict[str, Any]] = []

    for item in queue_payload["items"]:
        snapshot = signals["task_artifacts"].get(item["id"], {})
        if not snapshot.get("exists") or not snapshot.get("updated_at"):
            continue
        if not dependency_ready(item, completed_ids):
            continue
        snapshot_updated_at = snapshot["updated_at"]
        if item["id"] in completed_ids and artifact_history.get(item["id"]) == snapshot_updated_at:
            continue
        completed_ids.add(item["id"])
        artifact_history[item["id"]] = snapshot_updated_at
        advanced.append(
            {
                "signal": "task_artifact",
                "task_id": item["id"],
                "task_title": item["title"],
                "updated_at": snapshot_updated_at,
            }
        )

    pending_items = [item for item in queue_payload["items"] if item["id"] not in completed_ids]
    for signal_name in ("hq_plan", "mission_outbox"):
        snapshot = signals[signal_name]
        if snapshot["mode"] != "live" or not snapshot.get("updated_at"):
            continue
        if signal_history.get(signal_name) == snapshot["updated_at"]:
            continue
        next_item = next((item for item in pending_items if dependency_ready(item, completed_ids)), None)
        if not next_item:
            signal_history[signal_name] = snapshot["updated_at"]
            continue
        completed_ids.add(next_item["id"])
        signal_history[signal_name] = snapshot["updated_at"]
        advanced.append(
            {
                "signal": signal_name,
                "task_id": next_item["id"],
                "task_title": next_item["title"],
                "updated_at": snapshot["updated_at"],
            }
        )
        pending_items = [item for item in queue_payload["items"] if item["id"] not in completed_ids]

    progressed_items: List[Dict[str, Any]] = []
    for item in queue_payload["items"]:
        next_item = dict(item)
        if next_item["id"] in completed_ids:
            next_item["status"] = "done"
            next_item["blocked_by"] = []
        else:
            status = base_queue_status(next_item)
            if status == "queued" and dependency_ready(next_item, completed_ids):
                status = "ready"
            next_item["status"] = status
        progressed_items.append(next_item)

    current_focus = next(
        (item for item in progressed_items if item["status"] in {"ready", "at-risk"}),
        next((item for item in progressed_items if item["status"] == "blocked"), None),
    )

    summary = {
        "total": len(progressed_items),
        "done": sum(1 for item in progressed_items if item["status"] == "done"),
        "ready": sum(1 for item in progressed_items if item["status"] == "ready"),
        "at_risk": sum(1 for item in progressed_items if item["status"] == "at-risk"),
        "queued": sum(1 for item in progressed_items if item["status"] == "queued"),
        "blocked": sum(1 for item in progressed_items if item["status"] == "blocked"),
    }

    mission_state = {
        "completed_task_ids": [item["id"] for item in progressed_items if item["status"] == "done"],
        "signals": signal_history,
        "artifacts": artifact_history,
        "updated_at": now_utc_iso(),
        "current_focus_id": current_focus["id"] if current_focus else None,
    }
    state["missions"][mission_id] = mission_state
    save_progress_state(progress_state_path, state)

    queue_payload["items"] = progressed_items
    queue_payload["summary"] = summary
    queue_payload["current_focus"] = current_focus
    queue_payload["auto_progress"] = {
        "state_path": str(progress_state_path),
        "completed_task_ids": mission_state["completed_task_ids"],
        "signals": signals,
        "artifacts": artifact_history,
        "advanced": advanced,
    }
    return queue_payload


