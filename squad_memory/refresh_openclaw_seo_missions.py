def component_labels(stack: List[str], components_by_id: Dict[str, Dict[str, Any]]) -> List[str]:
    labels: List[str] = []
    for component_id in stack:
        component = components_by_id.get(component_id, {})
        labels.append(component.get("label", component_id))
    return labels


def digest_keyword_matches(workflow: Dict[str, Any], digest: Dict[str, Any]) -> List[str]:
    digest_blob = normalize_text(
        " ".join(
            [
                digest.get("headline", ""),
                *[item.get("title", "") for item in digest.get("alerts", [])],
                *[item.get("details", "") for item in digest.get("alerts", [])],
                *[item.get("title", "") for item in digest.get("opportunities", [])],
                *[item.get("why_now", "") for item in digest.get("opportunities", [])],
                *[item.get("action", "") for item in digest.get("opportunities", [])],
            ]
        )
    )
    matches: List[str] = []
    for keyword in workflow.get("goal_keywords", []):
        normalized = normalize_text(keyword)
        if normalized and normalized in digest_blob:
            matches.append(keyword)
    return matches


def stack_readiness(workflow: Dict[str, Any], components_by_id: Dict[str, Dict[str, Any]]) -> Tuple[int, List[str], List[str]]:
    score = 0
    reasons: List[str] = []
    blockers: List[str] = []
    ready_count = 0
    partial_count = 0

    for component_id in workflow["stack"]:
        component = components_by_id.get(component_id, {})
        status = component.get("status", "missing")
        label = component.get("label", component_id)
        if status == "ready":
            score += 7
            ready_count += 1
        elif status == "partial":
            score += 2
            partial_count += 1
            blockers.append(f"{label} is only partially ready.")
        else:
            score -= 8
            blockers.append(f"{label} is missing.")

    if ready_count == len(workflow["stack"]):
        reasons.append("Every required local component is ready.")
    elif ready_count > 0:
        reasons.append(f"{ready_count} required component(s) are fully ready.")
    if partial_count > 0:
        reasons.append(f"{partial_count} component(s) are only partially ready and may need operator cleanup.")

    return score, reasons, blockers


def execution_fit_score(workflow: Dict[str, Any], goal: str | None) -> Tuple[int, str]:
    fit = workflow.get("automation_fit", "ambient")
    if goal:
        if fit == "ambient":
            return 8, "The mission still fits an explicit operator goal without needing extra routing context."
        if fit == "topic":
            return 10, "This workflow converts a named topic or request into a concrete plan cleanly."
        return 7, "This workflow becomes more practical once a specific goal or problem statement is present."

    if fit == "ambient":
        return 12, "This workflow can self-start from ambient system signals and local status alone."
    if fit == "topic":
        return 6, "This workflow is useful automatically, but it becomes stronger when a topic is named explicitly."
    return 2, "This workflow is strongest once a specific site, URL set, or query set is supplied."


def signal_fit_score(workflow: Dict[str, Any], payload: Dict[str, Any], digest: Dict[str, Any]) -> Tuple[int, List[str]]:
    workflow_id = workflow["id"]
    pending_total = int(payload.get("pending", {}).get("pending_total", 0))
    recent_fetch_ok = int(payload.get("activity", {}).get("recent_fetch_ok", 0))
    recent_fetch_error = int(payload.get("activity", {}).get("recent_fetch_error", 0))
    phase = payload.get("activity", {}).get("current_phase", "unknown")
    live_articles = payload.get("recent_knowledge", {}).get("live_articles", [])
    primary_source_notes = int(payload.get("coverage", {}).get("primary_source_notes", 0))
    inactive_jobs = [job for job in payload.get("active_jobs", []) if job.get("status") != "active"]
    alert_titles = [item.get("title", "") for item in digest.get("alerts", [])]
    opportunity_titles = [item.get("title", "") for item in digest.get("opportunities", [])]
    keyword_hits = digest_keyword_matches(workflow, digest)

    score = min(len(keyword_hits) * 4, 12)
    reasons: List[str] = []
    if keyword_hits:
        reasons.append(f"Digest language already matches this workflow: {', '.join(keyword_hits[:4])}.")

    if workflow_id == "freshness-radar":
        score += 18
        if live_articles:
            score += min(len(live_articles) * 4, 20)
            reasons.append(f"{len(live_articles)} fresh article notes are already available for same-day exploitation.")
        if recent_fetch_ok > 0:
            score += min(recent_fetch_ok, 10)
            reasons.append(f"The refresh layer still has positive movement (`recent_fetch_ok={recent_fetch_ok}`).")
        if phase in {"discovering", "harvesting", "expanding", "building"}:
            score += 8
            reasons.append(f"The current phase is `{phase}`, which favors freshness-first triage.")
    elif workflow_id == "gsc-opportunity-mining":
        score += 16
        if any("opportunity" in title.lower() for title in opportunity_titles):
            score += 8
            reasons.append("The digest already surfaced opportunity-shaped work worth turning into a GSC queue.")
        if recent_fetch_error == 0:
            score += 4
            reasons.append("No active refresh instability is distracting from traffic-lift work.")
    elif workflow_id == "cannibalization-cleanup":
        score += 10
        if pending_total < 500:
            score += 4
            reasons.append("The backlog is manageable enough to spend time on structural cleanup.")
        if any("pattern" in title.lower() for title in opportunity_titles):
            score += 4
            reasons.append("Repeated source patterns suggest overlap and consolidation checks may pay off.")
    elif workflow_id == "indexation-war-room":
        score += 14
        if recent_fetch_error > 0:
            score += min(recent_fetch_error * 5, 18)
            reasons.append(f"Recent error activity is non-zero (`recent_fetch_error={recent_fetch_error}`).")
        if inactive_jobs:
            score += min(len(inactive_jobs) * 6, 18)
            reasons.append(f"{len(inactive_jobs)} tracked job(s) are not active, which raises operational stability questions.")
        if any("missing" in title.lower() or "idle" in title.lower() for title in alert_titles):
            score += 8
            reasons.append("The alert layer contains stale-job or missing-signal warnings.")
    elif workflow_id == "content-gap-to-brief":
        score += 12
        if live_articles:
            score += min(len(live_articles) * 3, 12)
            reasons.append("Fresh source notes can be converted into tighter content briefs immediately.")
        if primary_source_notes > 0:
            score += 6
            reasons.append(f"The local canon already includes `{primary_source_notes}` primary-source notes for grounding.")
        if any("fresh source exploitation" in title.lower() for title in opportunity_titles):
            score += 6
            reasons.append("The digest already points to fresh-source exploitation work.")
    elif workflow_id == "technical-audit-sprint":
        score += 12
        if recent_fetch_error > 0:
            score += min(recent_fetch_error * 4, 12)
            reasons.append("Recent errors justify a technical pass before scaling further.")
        if inactive_jobs:
            score += 6
            reasons.append("Background job instability makes a technical pass more defensible.")
        if primary_source_notes < 10:
            score += 5
            reasons.append(f"The primary-source layer is still thin (`primary_source_notes={primary_source_notes}`).")

    if not reasons:
        reasons.append(f"{workflow['name']} remains a valid reusable workflow for this stack even without a strong live signal spike.")
    return score, reasons


def hq_signal_score(workflow: Dict[str, Any], hq_context: Dict[str, Any]) -> Tuple[int, List[str]]:
    if not hq_context.get("available"):
        return 0, []

    workflow_id = workflow["id"]
    phase21 = hq_context.get("phase21", {})
    phase31 = hq_context.get("phase31", {})

    alerts_total = max(int(phase21.get("alerts_total", 0)), int(phase31.get("alerts_total", 0)))
    seo_hold = int(phase21.get("seo_queue_hold", 0))
    stale_sources = int(phase21.get("seo_stale_sources", 0))
    seo_new_items = int(phase21.get("seo_new_items", 0))
    topics_total = int(phase31.get("topics_total", 0))
    memory_paths_total = int(phase31.get("memory_paths_total", 0))
    links_total = int(phase31.get("links_total", 0))
    meetings_total = int(phase31.get("meetings_total", 0))
    primary_skill_accuracy = float(phase31.get("primary_skill_accuracy", 0.0))
    pass_rate = float(phase31.get("pass_rate", 0.0))

    score = 0
    reasons: List[str] = []

    if workflow_id == "freshness-radar":
        if meetings_total > 0:
            score += 4
            reasons.append(f"HQ already shows `{meetings_total}` active meeting snapshot(s), so fresh evidence can feed live coordination.")
        if alerts_total <= 12:
            score += 3
            reasons.append("HQ pressure is elevated but still manageable, so freshness exploitation remains defensible.")
        if seo_new_items > 0:
            score += min(seo_new_items, 4)
            reasons.append(f"HQ source health still reports `{seo_new_items}` new SEO item(s) in the latest control-plane snapshot.")
    elif workflow_id == "gsc-opportunity-mining":
        if stale_sources <= 3:
            score += 4
            reasons.append("HQ source health is stable enough to spend time on traffic wins instead of pure recovery.")
        if primary_skill_accuracy >= 0.95:
            score += 4
            reasons.append(f"Graph retrieval quality is strong (`primary skill accuracy={primary_skill_accuracy:.2%}`), which supports tighter opportunity ranking.")
    elif workflow_id == "cannibalization-cleanup":
        if memory_paths_total >= 3000:
            score += 6
            reasons.append(f"The memory graph already spans `{memory_paths_total}` paths, which is enough topology for overlap cleanup.")
        if links_total >= 200:
            score += 4
            reasons.append(f"The graph has `{links_total}` links, so consolidation work can use richer relationship context.")
    elif workflow_id == "indexation-war-room":
        if stale_sources > 0:
            score += min(stale_sources * 3, 12)
            reasons.append(f"HQ is already flagging `{stale_sources}` stale SEO source(s), which makes recovery and indexing triage more urgent.")
        if alerts_total >= 8:
            score += 4
            reasons.append(f"The HQ alert load is high (`{alerts_total}` alerts), so war-room style triage is justified.")
    elif workflow_id == "content-gap-to-brief":
        if topics_total >= 40:
            score += 6
            reasons.append(f"The graph already maps `{topics_total}` topics, so gap analysis has enough coverage to produce a sharper brief.")
        if memory_paths_total >= 3000:
            score += 4
            reasons.append("The graph corpus is large enough to turn missing coverage into a more grounded content brief.")
        if meetings_total > 0:
            score += 3
            reasons.append("HQ has recent meeting context, which supports packaging findings into a brief for the next handoff.")
    elif workflow_id == "technical-audit-sprint":
        if alerts_total >= 8:
            score += 6
            reasons.append(f"HQ is carrying `{alerts_total}` alerts, which increases the case for a technical stabilization pass.")
        if primary_skill_accuracy and primary_skill_accuracy < 0.96:
            score += 4
            reasons.append(f"Graph evaluation slipped below the ideal threshold (`primary skill accuracy={primary_skill_accuracy:.2%}`), so deeper technical cleanup is defensible.")
        if pass_rate and pass_rate < 0.99:
            score += 3
            reasons.append(f"Task pass rate is only `{pass_rate:.2%}`, which supports an audit sprint before scaling execution.")
        if seo_hold >= 30:
            score += 3
            reasons.append(f"The SEO HQ queue has `{seo_hold}` held items, which may indicate structural friction worth auditing.")

    return score, reasons


def load_route_history(router_dir: Path) -> List[Dict[str, Any]]:
    history_path = router_dir / "history.json"
    raw = load_json(history_path).get("entries", [])
    return [entry for entry in raw if isinstance(entry, dict)]


def save_route_history(history_path: Path, entries: List[Dict[str, Any]]) -> None:
    history_path.write_text(
        json.dumps({"entries": entries[-ROUTE_HISTORY_LIMIT:]}, indent=2, ensure_ascii=True) + "\n",
        encoding="utf-8",
    )


def cooldown_penalty(workflow: Dict[str, Any], history: List[Dict[str, Any]], generated_at: str) -> Tuple[int, List[str], Dict[str, Any] | None]:
    now_dt = parse_iso_datetime(generated_at)
    if not now_dt:
        return 0, [], None

    workflow_id = workflow["id"]
    cooldown_hours = int(workflow.get("cooldown_hours", 8))
    previous = None
    for entry in reversed(history):
        if entry.get("selected_id") == workflow_id:
            previous = entry
            break
    if not previous:
        return 0, [], None

    previous_dt = parse_iso_datetime(previous.get("generated_at"))
    if not previous_dt:
        return 0, [], None

    age_hours = max((now_dt - previous_dt).total_seconds() / 3600.0, 0.0)
    if age_hours >= cooldown_hours:
        return 0, [], {"last_selected_at": previous.get("generated_at"), "age_hours": round(age_hours, 1)}

    remaining_ratio = max((cooldown_hours - age_hours) / cooldown_hours, 0.0)
    penalty = max(2, round(12 * remaining_ratio))
    reasons = [
        f"This mission was selected {age_hours:.1f}h ago, so a cooldown penalty was applied to avoid repeating the same play too often."
    ]
    history_context = {
        "last_selected_at": previous.get("generated_at"),
        "age_hours": round(age_hours, 1),
        "cooldown_hours": cooldown_hours,
    }
    return penalty, reasons, history_context


def confidence_for(score: int, blockers: List[str], readiness_score: int, signal_score: int) -> str:
    if blockers or readiness_score < 6:
        return "low"
    if score >= 60 and signal_score >= 20:
        return "high"
    if score >= 38:
        return "medium"
    return "low"


def evaluate_workflow(
    workflow: Dict[str, Any],
    payload: Dict[str, Any],
    digest: Dict[str, Any],
    hq_context: Dict[str, Any],
    components_by_id: Dict[str, Dict[str, Any]],
    history: List[Dict[str, Any]],
    goal: str | None = None,
) -> Dict[str, Any]:
    strategic_value = int(workflow.get("impact", 3)) * 5
    efficiency = max(2, (5 - int(workflow.get("effort", 3))) * 3)
    readiness_score, readiness_reasons, blockers = stack_readiness(workflow, components_by_id)
    signal_score, signal_reasons = signal_fit_score(workflow, payload, digest)
    hq_score, hq_reasons = hq_signal_score(workflow, hq_context)
    execution_fit, execution_reason = execution_fit_score(workflow, goal)
    cooldown_value, cooldown_reasons, history_context = cooldown_penalty(workflow, history, now_utc_iso())

    matched_keywords: List[str] = []
    goal_fit = 0
    goal_mismatch_penalty = 0
    goal_reasons: List[str] = []
    if goal:
        normalized_goal = normalize_text(goal)
        goal_tokens = set(normalized_goal.split())
        matched_keywords = [
            keyword
            for keyword in workflow.get("goal_keywords", [])
            if normalize_text(keyword) and normalize_text(keyword) in normalized_goal
        ]
        name_overlap = sorted(goal_tokens & set(normalize_text(workflow["name"]).split()))
        goal_fit += len(matched_keywords) * 30
        goal_fit += len(name_overlap) * 8
        if matched_keywords:
            goal_reasons.append(f"Goal matched mission keywords: {', '.join(matched_keywords[:4])}.")
        if name_overlap:
            goal_reasons.append(f"Goal overlaps with workflow naming: {', '.join(name_overlap)}.")
        if not matched_keywords and not name_overlap:
            goal_mismatch_penalty = 12
            goal_reasons.append("This workflow does not match the explicit goal language directly.")

    breakdown = {
        "strategic_value": strategic_value,
        "signal_fit": signal_score,
        "hq_fit": hq_score,
        "readiness": readiness_score,
        "execution_fit": execution_fit,
        "efficiency": efficiency,
        "goal_fit": goal_fit,
        "goal_mismatch_penalty": -goal_mismatch_penalty,
        "cooldown_penalty": -cooldown_value,
    }
    score = (
        strategic_value
        + signal_score
        + hq_score
        + readiness_score
        + execution_fit
        + efficiency
        + goal_fit
        - goal_mismatch_penalty
        - cooldown_value
    )
    confidence = confidence_for(score, blockers, readiness_score, signal_score + hq_score + goal_fit)

    reasons = goal_reasons + signal_reasons + hq_reasons + readiness_reasons
    reasons.append(execution_reason)
    reasons.extend(cooldown_reasons)

    if not reasons:
        reasons.append(f"{workflow['name']} remains viable for this stack.")

    return {
        "workflow": workflow,
        "score": score,
        "confidence": confidence,
        "reasons": reasons[:6],
        "blockers": blockers[:4],
        "score_breakdown": breakdown,
        "matched_keywords": matched_keywords,
        "history": history_context,
    }


def build_route_payload(
    selected_eval: Dict[str, Any],
    ranked_evals: List[Dict[str, Any]],
    components_by_id: Dict[str, Dict[str, Any]],
    source: str,
    hq_context: Dict[str, Any],
    goal: str | None = None,
) -> Dict[str, Any]:
    workflow = selected_eval["workflow"]
    alternatives: List[Dict[str, Any]] = []
    for evaluation in ranked_evals[1:4]:
        alt_workflow = evaluation["workflow"]
        alternatives.append(
            {
                "id": alt_workflow["id"],
                "name": alt_workflow["name"],
                "score": evaluation["score"],
                "confidence": evaluation["confidence"],
                "score_gap": selected_eval["score"] - evaluation["score"],
                "why": evaluation["reasons"][0],
            }
        )

    switch_conditions = [
        f"Watch `{item['name']}` if its signal improves enough to close the current {item['score_gap']}-point gap."
        for item in alternatives
        if item["score_gap"] <= 10
    ]
    if not switch_conditions:
        switch_conditions.append("Stay with the current mission until a new alert, opportunity, or explicit goal changes the scoreboard materially.")

    return {
        "generated_at": now_utc_iso(),
        "source": source,
        "goal": goal,
        "score": selected_eval["score"],
        "confidence": selected_eval["confidence"],
        "reasons": selected_eval["reasons"][:4],
        "blockers": selected_eval["blockers"],
        "score_breakdown": selected_eval["score_breakdown"],
        "hq": {
            "available": hq_context.get("available", False),
            "ready": hq_context.get("ready", False),
            "seo_queue_hold": hq_context.get("phase21", {}).get("seo_queue_hold", 0),
            "seo_stale_sources": hq_context.get("phase21", {}).get("seo_stale_sources", 0),
            "graph_topics_total": hq_context.get("phase31", {}).get("topics_total", 0),
            "graph_memory_paths_total": hq_context.get("phase31", {}).get("memory_paths_total", 0),
        },
        "history": selected_eval["history"],
        "selected": {
            "id": workflow["id"],
            "name": workflow["name"],
            "objective": workflow["objective"],
            "best_when": workflow["best_when"],
            "cadence": workflow["cadence"],
            "impact": workflow.get("impact"),
            "effort": workflow.get("effort"),
            "automation_fit": workflow.get("automation_fit"),
            "cooldown_hours": workflow.get("cooldown_hours"),
            "stack": workflow["stack"],
            "stack_labels": component_labels(workflow["stack"], components_by_id),
            "output": workflow["output"],
            "outbox_path": f"automation/missions/outbox/{workflow['id']}.md",
            "prompt_path": f"automation/missions/prompts/{workflow['id']}.md",
            "prose_path": f"automation/missions/prose/{workflow['id']}.prose",
            "queue_path": f"automation/missions/execution/{workflow['id']}.md",
            "current_queue_path": "automation/missions/execution/current-queue.md",
        },
        "alternatives": alternatives,
        "switch_conditions": switch_conditions,
    }


def rank_workflows(
    workflows: List[Dict[str, Any]],
    payload: Dict[str, Any],
    digest: Dict[str, Any],
    hq_context: Dict[str, Any],
    components_by_id: Dict[str, Dict[str, Any]],
    history: List[Dict[str, Any]],
    goal: str | None = None,
) -> List[Dict[str, Any]]:
    ranked = [
        evaluate_workflow(
            workflow=workflow,
            payload=payload,
            digest=digest,
            hq_context=hq_context,
            components_by_id=components_by_id,
            history=history,
            goal=goal,
        )
        for workflow in workflows
    ]
    ranked.sort(key=lambda item: (-item["score"], item["workflow"]["name"]))
    return ranked


def choose_automatic_route(
    workflows: List[Dict[str, Any]],
    payload: Dict[str, Any],
    digest: Dict[str, Any],
    hq_context: Dict[str, Any],
    components_by_id: Dict[str, Dict[str, Any]],
    history: List[Dict[str, Any]],
) -> Tuple[Dict[str, Any], List[Dict[str, Any]]]:
    ranked = rank_workflows(workflows, payload, digest, hq_context, components_by_id, history)
    route = build_route_payload(ranked[0], ranked, components_by_id, source="automatic", hq_context=hq_context)
    return route, ranked


def route_goal(
    goal: str,
    workflows: List[Dict[str, Any]],
    payload: Dict[str, Any],
    digest: Dict[str, Any],
    hq_context: Dict[str, Any],
    components_by_id: Dict[str, Dict[str, Any]],
    history: List[Dict[str, Any]],
) -> Tuple[Dict[str, Any], List[Dict[str, Any]]]:
    ranked = rank_workflows(workflows, payload, digest, hq_context, components_by_id, history, goal=goal)
    if ranked[0]["score"] <= 0:
        automatic_route, automatic_ranked = choose_automatic_route(workflows, payload, digest, hq_context, components_by_id, history)
        automatic_route["source"] = "goal-fallback"
        automatic_route["goal"] = goal
        automatic_route["reasons"].insert(0, "No workflow keyword matched directly, so the router fell back to the strongest automatic mission.")
        return automatic_route, automatic_ranked
    return build_route_payload(ranked[0], ranked, components_by_id, source="goal", hq_context=hq_context, goal=goal), ranked


